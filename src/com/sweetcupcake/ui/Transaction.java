     private void refreshTable() {
        try {
           List<Cupcake> cupcakes = cupcakeDAO.getAllCupcakes();
           String[] columns = {"Cupcake ID", "Cupcake Name", "Category", "Price", "Quantity", "Status"};
           Object[][] data = new Object[cupcakes.size()][columns.length];
           
           for(int i = 0; i <cupcakes.size(); i++){
               Cupcake c = cupcakes.get(i);
               data[i][0] = c.getId();
               data[i][1] = c.getName();
               data[i][2] = c.getCategory();
               data[i][3] = c.getPrice();
               data[i][4] = c.getQuantity();
               data[i][5] = c.getStatus();
           }
           jTable1.setModel(new javax.swing.table.DefaultTableModel(data, columns));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
     
    private void handlejTextField1Released(java.awt.event.KeyEvent evt){
        String idText = jTextField1.getText().trim();
        if(jTextField1.getText().trim().isEmpty()){
            clearfields.clear(jTextField2, jTextField3, jTextField4,jTextField5,jLabel9);
            return;
        }
        
        try{
            int jTextField1 = Integer.parseInt(idText);
            fillCupcakeDetails(jTextField1);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
}

    private void fillCupcakeDetails(int jTextField1){
        try{
            String sql = "SELECT Cupcake_Name, Category, Price FROM cupcake WHERE Cupcake_ID = ?";
            pst = conn.prepareStatement(sql);
            pst.setInt(1, jTextField1);
            rs = pst.executeQuery();
           
          if(rs.next()){
              jTextField2.setText(rs.getString("Cupcake_Name"));
              jTextField3.setText(rs.getString("Category"));
              jTextField4.setText(String.format("%.2f",rs.getDouble("Price")));   
          }else{
              JOptionPane.showMessageDialog(this, "Please Enter Valid Cupcake ID!");
              clearfields.clear(jTextField2, jTextField3, jTextField4, jTextField5);
              
          }
        }catch(Exception e){
         JOptionPane.showMessageDialog(null, e);
     }
    
}
