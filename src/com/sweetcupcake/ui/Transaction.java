    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
        int cupcakeId = Integer.parseInt(jTextField1.getText().trim());
        int quantity = Integer.parseInt(jTextField5.getText().trim());

        Cupcake cupcake = cupcakeDAO.getAllCupcakes()
                .stream()
                .filter(c -> c.getId() == cupcakeId)
                .findFirst()
                .orElse(null);

        if (cupcake == null) {
            JOptionPane.showMessageDialog(this, "Invalid Cupcake ID!");
            return;
        }
        Bill bill = new Bill(cupcake, quantity);
        boolean success = billDAO.saveBill(bill);
          
        if(success){
            boolean updated = cupcakeDAO.updateQuantity(cupcake.getId(), quantity);
            if(!updated){
                JOptionPane.showMessageDialog(this, "Sale recorded, but stock update failed!");
            }
            
            jLabel9.setText(String.format("LKR %.2f", bill.getTotal()));
            JOptionPane.showMessageDialog(this, " Bill Saved Successfully!");
        
            clearfields.clear(jTextField1, jTextField2, jTextField3, jTextField4, jTextField5,jLabel9);
            refreshTable();
         }else{
            JOptionPane.showMessageDialog(this, " Failed to record sale.");
        }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }    
    }//GEN-LAST:event_jButton2ActionPerformed

