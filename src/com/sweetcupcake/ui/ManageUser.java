    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
          try {
        int userId = Integer.parseInt(jTextField1.getText().trim());
        String fullName = jTextField2.getText().trim();
        String contact = jTextField3.getText().trim();
        String role = jTextField4.getText().trim();
        String username = jTextField5.getText().trim();
        String password = jTextField6.getText().trim();

        // Make sure required fields are filled for ADD/UPDATE
        if (currentAction.equals("ADD") || currentAction.equals("UPDATE")) {
            if (fullName.isEmpty() || contact.isEmpty() || role.isEmpty() || username.isEmpty() || password.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill all required fields!");
                clearfields.clear(jTextField1,jTextField2, jTextField3, jTextField4, jTextField5, jTextField6 );
                return;
            }
        }

        boolean result = false;

        switch (currentAction) {
            case "ADD":
                result = authController.getUserDao().addUser(new User(userId, fullName, contact, role, username, password));
                if (result) JOptionPane.showMessageDialog(this, "User added successfully!");
                break;

            case "UPDATE":
                result = authController.getUserDao().updateUser(new User(userId, fullName, contact, role, username, password));
                if (result) JOptionPane.showMessageDialog(this, "User updated successfully!");
                else JOptionPane.showMessageDialog(this, "Invalid User ID!");
                break;

            case "DELETE":
                result = authController.getUserDao().deleteUser(userId);
                if (result) JOptionPane.showMessageDialog(this, "User deleted successfully!");
                else JOptionPane.showMessageDialog(this, "Invalid User ID!");
                break;

            default:
                JOptionPane.showMessageDialog(this, "Please select an action first!");
                return;
        }

        UpdateTable();   // Refresh the JTable
         clearfields.clear(jTextField1,jTextField2, jTextField3, jTextField4, jTextField5, jTextField6 );  
        currentAction = ""; // Reset currentAction

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "User ID must be a number!");
        clearfields.clear(jTextField1,jTextField2, jTextField3, jTextField4, jTextField5, jTextField6 );
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton4ActionPerformed

