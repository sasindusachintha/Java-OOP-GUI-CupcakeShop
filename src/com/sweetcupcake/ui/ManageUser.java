    private void UpdateTable(){
        try {
       
        java.util.List<User> users = authController.getAllUsers();
        
        // Convert List<User> to TableModel
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
            new String[]{"User_ID","Full_Name","Contact","Role","Username","Password"}, 0
        );

        for (User u : users) {
            model.addRow(new Object[]{
                u.getId(), u.getFullName(), u.getContact(), u.getRole(), u.getUsername(), u.getPassword()
            });
        }

        jTable1.setModel(model);
        jTable1.setDefaultEditor(Object.class, null);
    } catch(Exception e) {
        JOptionPane.showMessageDialog(this, e);
    }
    }
    
